 <b>Lentejas con chorizo<br/></b>
 <img src="lentejas.jpg" alt="imagen de lentejas"/><br/>
 Tiempo de realización 1,5 horas<br/>
<u>Ingredientes para 6 personas:<br/></u> 
<ul>
<li>400g de lentejas</li>
<li>1/2 cebolla</li>
<li>1 zanahoria</li>
<li>1 pimiento verde</li>
<li>1 tomate </li>
<li>1 hoja de laurel</li>
<li>1 cabeza de ajo </li>
<li>1 cucharadita de pimentón </li>
<li>1 chorito de aceite </li>
<li>200 g de chorizo </li>
<li>1 patata grande (opcional)</li>
</ul>
<u>Realización:<br/> </u>
Se pone todo en una cazuela (excepto las patatas) en crudo, 
se pone a cocer a fuego lento hasta que estén tiernas(1,5 horas 
aproximadamente)añadiendole agua fría siempre que haga falta,
.Si se echan patatas, se echan cortadas en daditos pequeños 20 
minutos antes de terminar la cocción.Si quedan caldosas se le puede 
añadir una cucharadita de pan rallado 
