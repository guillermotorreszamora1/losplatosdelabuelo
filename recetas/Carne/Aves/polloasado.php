<b>Cuartos traseros de pollo asados en su jugo <br/></b>
 <img src="polloasado.jpg" alt="imagen de pollo asado"/><br/>
<u>Ingredientes para 6 personas:<br/></u> 
<ul>
<li>6 cuartos traseros de pollo</li>
<li>sal y pimienta</li>
<li>1 vaso Vino blanco seco </li>
<li>1/2 vaso de Agua </li>
</ul>
<u>Realización:<br/> </u>
Calienta el horno a 200ºC, 
salpimentar el pollo colocarlo  en una fuente de 
asar con la piel hacia abajo, agregar el vino y el agua, 
meterlo en el horno durante 3/4 de hora darle la vuelta , 
regarlo con el caldo de la bandeja e introducirlo otros 
3/4 de hora , sacar y servir . Se puede acompañar con 
patatas asadas.  
