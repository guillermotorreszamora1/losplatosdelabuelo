 finita ponerla a pochar agregar los tomates 
 partidos y pelados , la sal y el azúcar , 
 agregar el orégano , cuando este 1 hora 
 aproximadamente agregar el vino y dejar 
 ½ hora más , pasarlo por el pasapuré e 
