<b>Estofado de buey<br/></b>
 <img src="estofadobuey.jpg" alt="imagen de estofado de buey"/><br/>
<u>Ingredientes para 6 personas:<br/></u> 
<ul>
<li>1,5 Kg de Morcillo de buey  </li>
<li>1 cebolla grande </li>
<li>2 ajos </li>
<li>1 pimiento </li>
<li>2 zanahorias </li>
<li>2 tomates</li>
<li>1 botella de Vino tinto</li> 
<li>Sal y pimienta</li> 
<li>Laurel </li>
<li>Agua</li>  
<li>0,5kg de patatas (opcional)</li>
</ul>
<u>Realización:<br/> </u>
Corta las verduras y ponlas a pochar en aceite
, corta la carne en trozos  e incorpórala a las
 verduras , rehoga durante 10 minutos y cúbrela
 con el vino tinto , ponlo a cocer lentamente
 durante 2 horas aproximadamente , añadiendo
 agua si lo necesita, comprueba que este tierna
 y si es necesario cuece un poco más sin dejar
 que se seque. Añade 0,5 kilos de patatas
 (opcional) y deja cocer 1/4 de hora más y servir.
