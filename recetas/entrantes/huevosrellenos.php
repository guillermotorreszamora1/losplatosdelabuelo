<b>Huevos rellenos<br/></b>
 <img src="huevosrellenos.jpg" alt="imagen de huevos rellenos"/><br/>
<u>Ingredientes para 4 personas:<br/></u> 
<ul>
<li>9 Huevos</li>
<li>2 latas de bonito </li>
<li>Aceite de girasol </li>
<li>Sal</li>
<li>Tomate frito </li>
<li>1 pimiento morrón o de piquillo (opcional) </li>

</ul>
<u>Realización:<br/> </u>
Cuece 8 huevos, cuando estén cocidos pártelos por la mitad y quítales la yema y apártalas.
 Con el otro huevo  y el aceite de girasol haz una mayonesa, coloca los medios huevos en
 una fuente y en un vaso de la batidora pos 6 yemas, agrega las dos latas de bonito
 desmenuzadas y un poco de tomate frito, de la mayonesa confeccionada echa dos o tres
 cucharadas y bátelo todo bien, rellena con esa mezcla los huevos, por encima una cucharadita
 de la mayonesa y las dos yemas de huevos espolvoreadas por encima. Si quieres corta un
 pimiento, para colocar un trozo sobre cada medio huevo para adornar. 
