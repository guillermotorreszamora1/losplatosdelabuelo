<b>Salmorejo<br/></b>
 <img src="salmorejo.jpg" alt="imagen de salmorejo"/><br/>
<u>Ingredientes<br/></u> 
<ul>
<li>4 o 5 ajos</li> 
<li>1,5 kg de tomate maduros muy rojos </li>
<li>1/2 barra de pan candeal </li>
<li>100ml de aceite </li>
<li>Sal </li>
<li>Guarnición:<ul>
<li>2 huevos cocidos  picados finitos</li>
<li>Jamón serrano picado finito</li>
</ul></li></ul>
<u>Realización:<br/> </u>
Emulsiona el aceite con los ajos usando la batidora, 
échalo a un cuenco, tritura los tomates y pásalos
 por el chino y júntalo con el aceite, remoja el pan 
 con agua, bátelo bien por la batidora y pásalo por chino,
 incorporándolo todo a la cazuela, echa sal al gusto, 
 bátelo bien y ponlo a enfriar. 
La guarnición se la  sirve cada comensal en su taza 
