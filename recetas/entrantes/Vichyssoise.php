<b>Vichyssoise<br/></b>
 <img src="Vichyssoise.jpg" alt="imagen de vichyssoise"/><br/>
<u>Ingredientes para 10 personas:<br/></u> 
<ul>
<li>1 cebolla grande </li>
<li>4 puerros (solo la parte blanca)</li>
<li>3 patatas gordas (aproximadamente 1/2 kilo)</li>
<li>1 cucharada sopera de Mantequilla</li>
<li>1 litro de Caldo </li>
<li>1 litro de leche (aproximadamente)</li>
<li>200g de nata para cocinar </li>
</ul>
<u>Realización:<br/> </u>
Pon la mantequilla en una cazuela, agrega
 la cebolla picada, los puerros picados finos,
 dale una vuelta, agrega las patatas cortadas
 como si fuesen para tortilla y el caldo, cuece
 durante 1/2 hora apártalo del fuego, agrega la
 leche y pásalo todo por la túrmix, cuélalo
 por el chino, agrega la nata y ponla a enfriar,
 al servir puedes ponerle un poco de pimienta 
