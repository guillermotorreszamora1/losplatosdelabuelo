<b>Ensaladilla rusa<br/></b>
 <img src="ensaladilla.jpg" alt="imagen de ensaladilla"/><br/>
<u>Ingredientes para 10 personas:<br/></u> 
<ul>
<li>1kg de patatas </li>
<li>5 huevos </li>
<li>1/2 litro de aceite de girasol </li>
<li>Sal </li>
<li>Un poco de limón </li>
<li>1/2 kg de bonito en aceite </li>
<li>100g de aceitunas sin hueso </li>
</ul>
<u>Realización:<br/> </u>
Cuece las patatas sin pelar junto con los 4 huevos y un poco de sal,
 pínchalas con un tenedor para saber cuándo están tiernas.
 Aparta y enfría, mientras enfría preparas una mayonesa con 
 1 huevo, el aceite, un poco  de sal y un poco de limón. Pela
 las patatas y córtalas en daditos pequeños , agrega el bonito
 desmenuzado , las aceitunas y 3 huevos de los cocidos partidos
 pequeños , agrega la mayonesa , remueve bien y adorna con el 
 huevo que queda en rodajas finas , Enfriar y servir.
